public class MyFirstJavaProgram {
    public static void main(String[] args) {
        System.out.println("Hello, this is my first java program");
        System.out.println("");

        for(int i=0; i<5; i++) {
            System.out.println("Iteration # " + i);
        }
        System.out.println("");

        int j=0;
        while(j<5) {
            System.out.println("Iteration # " + j);
            j++;
        }
        System.out.println("");

        
    }
}